export function findPrefecture(postalCode: string | number): string | null {
  // 数値の場合は文字列に変換
  const code = typeof postalCode === 'number' ? postalCode.toString().padStart(7, '0') : postalCode;
  // 先頭2桁を取得
  const prefix = code.substring(0, 2);
  return prefectureMap[prefix] || null;
} 